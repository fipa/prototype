class UsersController < ApplicationController
  # GET /users
  # GET /users.json
  def index
    @users = User.all

    respond_to do |format|
      format.html # index.html.erb
      format.json { render :json => @users }
    end
  end

  # GET /users/1
  # GET /users/1.json
  def show
    @user = User.find(params[:id])

    respond_to do |format|
      format.html # show.html.erb
      format.json { render :json => @user }
    end
  end

  # GET /users/new
  # GET /users/new.json
  def new
    @user = User.new

    3.times {@user.gifts.build}

    respond_to do |format|
      format.html # new.html.erb
      format.json { render :json => @user }
    end
  end

  # GET /users/1/edit
  def edit
    @user = User.find(params[:id])
  end

  # POST /users
  # POST /users.json
  def create
    @user = User.new(params[:user])

    respond_to do |format|
      if @user.save
        format.html { redirect_to @user, :notice => 'User was successfully created.' }
        format.json { render :json => @user, :status => :created, :location => @user }
      else
        format.html { render :action => "new" }
        format.json { render :json => @user.errors, :status => :unprocessable_entity }
      end
    end
  end

  # PUT /users/1
  # PUT /users/1.json
  def update
    @user = User.find(params[:id])

    @user.gifts.each do |gift|
        gift.update_attributes(params['gift'][gift.id.to_s])
    end

    params[:gift_categories].each do |gc|
      gift_id = gc.first.split('_').first
      category_id = gc.first.split('_').last
      is_active = gc.last

      gift_category = GiftCategory.find(:first, :conditions => "category_id = " + category_id + " AND gift_id = " + gift_id)
      if is_active == '1'
        GiftCategory.create(:category_id => category_id, :gift_id => gift_id) if gift_category.nil?
      else
        gift_category.destroy unless gift_category.nil?
      end
    end

    respond_to do |format|
      if @user.update_attributes(params[:user])
        format.html { redirect_to :action => "edit"}
        format.json { head :ok }
      else
        format.html { render :action => "edit" }
        format.json { render :json => @user.errors, :status => :unprocessable_entity }
      end
    end
  end

  # DELETE /users/1
  # DELETE /users/1.json
  def destroy
    @user = User.find(params[:id])
    @user.destroy

    respond_to do |format|
      format.html { redirect_to users_url }
      format.json { head :ok }
    end
  end
end
